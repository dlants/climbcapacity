import { describe, test, expect } from "vitest";
import { MEASURES } from "./index.js";

describe("measures", () => {
  test("should contain expected anthro measures", () => {
    const anthroMeasures = MEASURES.filter((m) => m.type === "anthro");
    expect(anthroMeasures.map((m) => m.id)).toEqual([
      "height",
      "armspan",
      "standing-reach",
      "weight",
      "sex-at-birth",
    ]);
  });

  test("all measures have non-empty ids", () => {
    expect(MEASURES.every((m) => m.id.length > 0)).toBe(true);
  });

  test("measure ids are unique", () => {
    const ids = new Set(MEASURES.map((m) => m.id));
    expect(ids).toEqual(new Set(EXPECTED_MEASURE_IDS));
  });
});

